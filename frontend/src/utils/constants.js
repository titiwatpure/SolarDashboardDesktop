// ฟังก์ชันคำนวณประเภทใบอนุญาตโดยอัตโนมัติ
// ใช้สำหรับแสดงตัวอย่างบนหน้า Frontend (ก่อนส่งสร้าง Backend)
export const determinePermitType = (sizeKva, hasPowerSelling) => {
  if (hasPowerSelling || (sizeKva && sizeKva > 1000)) {
    return 'permit';     // ขอใบอนุญาต
  } else if (sizeKva && sizeKva <= 1000 && !hasPowerSelling) {
    return 'exemption';  // แจ้งยกเว้น
  }
  return null; // ไม่เข้าเงื่อนไขใดๆ
};
